     public boolean validateForm() {
	  if (txtEmail.getText().trim().equals("")) {
	       alertWarning.setText("Email is not empty");
	       return false;
	  } else {
	       alertWarning.setText("");
	  }
	  if ((txtPassword.getPassword().length > 0) && (txtConfirmPassword.getPassword().length > 0)) {
	       alertWarning.setText("");
	       if (!(new String(txtPassword.getPassword()).equals(new String(txtConfirmPassword.getPassword())))) {
		    alertWarning.setText("Password is not match");
		    return false;
	       } else {
		    alertWarning.setText("");
	       }
	  } else {
	       alertWarning.setText("Password is requied");
	       return false;
	  }
	  if (txtPhoneNumber.getText().trim().equals("")) {
	       alertWarning.setText("Phone number is required");
	       return false;
	  } else {
	       alertWarning.setText("");
	  }
	  return true;
     }
     
     public static String encrypt(String key, String initVector, String password) {
	  try {
	       IvParameterSpec ivp = new IvParameterSpec(initVector.getBytes());

	       SecretKeySpec skey = new SecretKeySpec(key.getBytes(), "AES");

	       Cipher cipher = Cipher.getInstance("AES/CBC/PKCS5Padding");

	       cipher.init(Cipher.ENCRYPT_MODE, skey, ivp);

	       byte[] encryptByte = cipher.doFinal(password.getBytes());
	       return Base64.encodeBase64String(encryptByte);
	  } catch (Exception e) {
	       e.printStackTrace();
	       return "";
	  }

     }
     
